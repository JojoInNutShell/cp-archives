signed main() {
	ios_base::sync_with_stdio(0);cin.tie(nullptr); cout.tie(nullptr);
	ll n, k;
	cin >> n >> k;
	
	ll l = 1, r = (1ll << n)  - 1;
	ll c = n;
	while(l < r) {
		ll mid = (l + r)/2;
		if(mid == k) break;
		
		c--;
		if(k > mid) l = mid + 1;
		else r = mid - 1;
	}
	cout << c << endl;
	return 0;
}